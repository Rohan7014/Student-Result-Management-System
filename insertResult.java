    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField1.getText();
        String em=jTextField2.getText();
        String dbms=jTextField3.getText();
        String os=jTextField4.getText();
        String maths=jTextField5.getText();
        String ml=jTextField6.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            java.sql.Connection con=DriverManager.getConnection("jdbc:mysql://localhost:/srm","root","1234");
            java.sql.Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result values('"+rollno+"','"+em+"','"+dbms+"','"+os+"','"+maths+"','"+ml+"')");
                JOptionPane.showMessageDialog(null,"Data updated successfully!");
                setVisible(false);
                new insertResult().setVisible(true);
            }
            else{
                 JOptionPane.showMessageDialog(null,"Roll Number does not exist!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton12ActionPerformed

